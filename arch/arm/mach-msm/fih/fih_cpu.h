#ifndef __FIH_CPU_H
#define __FIH_CPU_H

/* Refer the 80-NP408-2X MSM8909-MSM8209-MSM8208 HARDWARE REGISTER DESCRIPTION DOCUMENT FOR OEMS.pdf
	FIH_MSM_HW_REV_NUM is mean JTAG_ID(0x0005E07C) (P.2806)
	FIH_MSM_HW_FEATURE_ID is mean QFPROM_CORR_JTAG_ID[bits 27:20](0x0005C000) (P.2767)
	FIH_MSM_SERIAL_NO is mean QFPROM_CORR_SERIAL_NUM(0x0005C008) (P.2767)
*/
#define FIH_MSM_HW_REV_NUM  0x0005E07C
#define FIH_MSM_HW_FEATURE_ID  0x0005C000
#define FIH_MSM_SERIAL_NO   0x0005C008

/* Refer the 80-NP408-4 MSM8909-MSM8609-MSM8209-MSM8208 DEVICE REVISION GUIDE
*/
#define HW_REV_NUM_MSM8909	0x009600E1
#define HW_REV_NUM_MSM8609	0x009840E1
#define HW_REV_NUM_MSM8209	0x009610E1
#define HW_REV_NUM_MSM8208	0x009620E1

#define HW_REV_ES 0x00
#define HW_REV_CS 0x10

#endif /* __FIH_CPU_H */
